    public String getTextoPersonaje(){
        String txt="";
        
        while(txt.equals("")){
        txt= txtPersonaje.getText();
        txtPersonaje.setText("");
        }
        
        return txt;
    }
    
    public ImageIcon getImgPersonaje(){
        JFileChooser chooser=new JFileChooser();
        int result= chooser.showOpenDialog(null);
        
        if(result==JFileChooser.APPROVE_OPTION){
            File imgSel =chooser.getSelectedFile();
            imgPersonaje = new ImageIcon(imgSel.getPath());
            Image img=imgPersonaje.getImage().getScaledInstance(imagen.getWidth(),imagen.getHeight(), Image.SCALE_SMOOTH);
            imgPersonaje=new ImageIcon(img);
            
        }
        return imgPersonaje;
    }
    
    public void mostrarImagen(ImageIcon image){
        try{
          Image img= image.getImage().getScaledInstance(imagen.getWidth(), imagen.getHeight(), Image.SCALE_SMOOTH);
          this.imagen.setIcon(new ImageIcon(img));
        }catch(Exception e){
            this.imagen.setIcon(sinImagen());
        }
    }
    
    public ImageIcon sinImagen(){
        ImageIcon image = new ImageIcon(getClass().getResource("/imagenes/noImagenPersonaje.png"));
        Image img=image.getImage().getScaledInstance(imagen.getWidth(), imagen.getHeight(), WIDTH);
        image=new ImageIcon(img);
        return image;
    
    }
    
    
