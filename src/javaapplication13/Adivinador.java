    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if(arbol==null){
            if(txtPersonaje.getText().isBlank()){
                JOptionPane.showMessageDialog(this, "Ingrese un personaje");
            }
            else{
            arbol=new arbolAVL(this);
            arbol.arbolInicio(txtPersonaje.getText());
            arbol.setPausado(true);

            JOptionPane.showMessageDialog(this, "se ha ingresado personaje, puedes iniciar a jugar");

            txtPersonaje.setVisible(false);
            btnRegistrar.setVisible(false);
            btnIniciar.setEnabled(true);
            btnIniciar.setVisible(true);
            lblResultado.setVisible(false);

            }
        }
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

